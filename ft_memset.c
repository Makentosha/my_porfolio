#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*pnt;

	pnt = (char *)b;
	i = 0;
	while (i < len)
	{
		pnt[i] = c;
		i++;
	}
	return (b);
}
