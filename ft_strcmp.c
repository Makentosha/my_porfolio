#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	const char *str2;
	const char *str1;
	int i;

	str1 = s1;
	str2 = s2;
	if (s1 == s2)
		return 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return 0;
}