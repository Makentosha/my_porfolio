#include <unistd.h>
#include <stdio.h>

int ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int main()
{
	char str[100] = "testd";
	int x = ft_strlen(str);
	printf("%d\n", x);
}