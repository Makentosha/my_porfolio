#include <string.h>
#include <unistd.h>
#include <stdio.h>

size_t	ft_strlen(const char *s)
{
	size_t i;

	i = 0;
	while (s[i])
	{
		i++;
	}
	return (i);
}

int main()
{
	char str[100] = "asdsdgfsdfj\0dhfgzsgfhdgsfdhfdghsgfhdgfsrehfhesrhtrdh";
	printf("ft_strlen: %lu\n", ft_strlen(str));
	printf("   strlen: %lu\n", strlen(str));
	return 0;
}